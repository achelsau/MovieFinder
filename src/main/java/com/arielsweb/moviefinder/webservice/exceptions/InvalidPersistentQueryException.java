package com.arielsweb.moviefinder.webservice.exceptions;

/**
 * Signals that the persistent query to be stored is malformed (either null or
 * empty query string OR negative interval)
 * 
 * @author Ariel
 * 
 */
public class InvalidPersistentQueryException extends Exception {

    private static final long serialVersionUID = -2422320379942244297L;

    /**
     * Creates a new exception
     * 
     * @param message
     *            the message to display to the user
     */
    public InvalidPersistentQueryException(String message) {
	super(message);
    }
}
