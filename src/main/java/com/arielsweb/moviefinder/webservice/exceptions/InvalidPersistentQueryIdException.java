package com.arielsweb.moviefinder.webservice.exceptions;

/**
 * Signals that the id of the persistent query is malformed
 * 
 * @author Ariel
 * 
 */
public class InvalidPersistentQueryIdException extends Exception {

    private static final long serialVersionUID = -8337316691955767330L;

    /**
     * Creates a new exception
     * 
     * @param message
     *            the message to be displayed to the user making the call
     */
    public InvalidPersistentQueryIdException(String message) {
	super(message);
    }
}
