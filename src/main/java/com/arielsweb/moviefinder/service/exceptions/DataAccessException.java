package com.arielsweb.moviefinder.service.exceptions;

/**
 * Runtime exception thrown when a data access service encounters an error in
 * its execution: for instance it receives a null id based on which it needs to
 * do a query.
 * 
 * @since 26/11/2011
 * @author Ariel
 */
public class DataAccessException extends RuntimeException {

    private static final long serialVersionUID = 1L;

    public DataAccessException() {
    }

    /**
     * @param message
     */
    public DataAccessException(String message) {
	super(message);
    }

    /**
     * @param cause
     */
    public DataAccessException(Throwable cause) {
	super(cause);
    }

    /**
     * @param message
     * @param cause
     */
    public DataAccessException(String message, Throwable cause) {
	super(message, cause);
    }

}
